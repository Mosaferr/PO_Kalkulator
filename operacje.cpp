#include "operacje.h"

double Kalkulator::dodaj(double skladnik1, double skladnik2)
{
	return (skladnik1 + skladnik2);
}
double Kalkulator::odejmij(double odjemna, double odjemnik)
{
	return (odjemna - odjemnik);
}
double Kalkulator::pomnoz(double czynnik1, double czynnik2)
{
	return (czynnik1 * czynnik2);
}
double Kalkulator::podziel(double dzielna, double dzielnik)
{
	return (dzielna / dzielnik);
}